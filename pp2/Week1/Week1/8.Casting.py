x = int(7) # x will be 7
y = int(7.6) # y will be 8
z = int ("2") # z will be 2

print(x)
print(y)
print(z)

# just like this we can also use float and string

a = float(20) # a will be 20.0
b = float(3) # b will be 3.0

print(a)
print(b)

c = str("s1") # c will be 's1'
d = str(7) # d will be '7'

print(c)
print(d)